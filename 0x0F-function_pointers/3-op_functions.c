/**
* op_add - functions to sum two int a and b
* @a: first integer operand
* @b: second integer operand
*
* Return: sum of int a and int b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - function to find the difference between two numbers
* @a: first int operand
* @b: second int operand
*
* Return: difference of int a and int b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - function to ,ultiply two int
* @a: first int operand
* @b: second int operand
*
* Return: product of int a and int b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - function to divide two integer numbers
* @a: frst integer operand
* @b: second integer operand
*
* Return: the value of a divided by b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - function to return the reminder from two int values
* @a: first operand
* @b: second operand
*
* Return: modulus divition of int a and b
*/
int op_mod(int a, int b)
{
	return (a % b);
}

